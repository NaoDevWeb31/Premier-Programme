/****** Modifier les attributs d'un élément avec jQuery et JavaScript ******/

// Avec JavaScript
document.querySelector("h1").getAttribute("class"); // title
document.querySelectorAll("p");

// Avec jQuery
$("h1").attr("class"); // sélectionner un attribut
$("p");
