/****** Sélectionner un élément avec jQuery et JavaScript ******/

// Avec JavaScript
document.querySelector("h1");
document.querySelectorAll("p");

// Avec jQuery
$("h1"); // On donne le sélecteur CSS
$("p"); // On sélectionne tous les paragraphes
